-- ============================================================================
-- Fase 1 RBAC: Schema replicado do Urânia Hub + seed inicial dos 5 usuários
-- ============================================================================
-- public.users_new:    espelho identitário (id = UUID do Hub; auth_user_id nullable)
-- public.departments:  catálogo hierárquico
-- public.platforms:    catálogo + entry "bi" local
-- public.user_departments, user_platform_access: relacionamentos
-- public.route_access_policies/rules + department_route_access: RBAC runtime
-- public.system_logs, public.tools

-- Nota: users_new vai ser renomeado para users após descomissionar user_profiles.

CREATE TABLE IF NOT EXISTS public.users_new (
  id uuid PRIMARY KEY,
  auth_user_id uuid UNIQUE REFERENCES auth.users(id) ON DELETE SET NULL,
  email text NOT NULL UNIQUE,
  full_name text,
  avatar_url text,
  job_title text,
  is_active boolean NOT NULL DEFAULT true,
  is_global_admin boolean NOT NULL DEFAULT false,
  kommo_user_id integer,
  teams_user_id text,
  phone_whatsapp text,
  notification_preferences jsonb DEFAULT '{"email":true,"whatsapp":false,"teams":false}'::jsonb,
  synced_from_hub_at timestamptz,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.departments (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug varchar NOT NULL UNIQUE,
  name varchar NOT NULL,
  description text,
  parent_department_id integer REFERENCES public.departments(id),
  color varchar,
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.platforms (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug varchar NOT NULL UNIQUE,
  name varchar NOT NULL,
  description text,
  icon varchar,
  color varchar DEFAULT '#3b82f6',
  base_url text,
  is_active boolean DEFAULT true,
  display_order integer DEFAULT 0,
  default_role text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.user_departments (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES public.users_new(id) ON DELETE CASCADE,
  department_id integer NOT NULL REFERENCES public.departments(id) ON DELETE CASCADE,
  is_primary boolean DEFAULT false,
  role text NOT NULL DEFAULT 'member' CHECK (role IN ('member', 'leader')),
  created_at timestamptz DEFAULT now(),
  UNIQUE(user_id, department_id)
);

CREATE TABLE IF NOT EXISTS public.user_platform_access (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES public.users_new(id) ON DELETE CASCADE,
  platform_id integer NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  role text NOT NULL DEFAULT 'viewer' CHECK (role IN ('viewer', 'user', 'manager', 'admin')),
  permissions jsonb DEFAULT '{}'::jsonb,
  created_at timestamptz DEFAULT now(),
  UNIQUE(user_id, platform_id)
);

CREATE TABLE IF NOT EXISTS public.route_access_policies (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  platform_id integer NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  route text NOT NULL,
  label text NOT NULL,
  managed_by text NOT NULL DEFAULT 'global_admin' CHECK (managed_by IN ('global_admin','platform_admin')),
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  UNIQUE(platform_id, route)
);

CREATE TABLE IF NOT EXISTS public.route_access_rules (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  policy_id integer NOT NULL REFERENCES public.route_access_policies(id) ON DELETE CASCADE,
  rule_type text NOT NULL CHECK (rule_type IN ('global_admin','platform_admin','department_role','entity_participant','specific_user')),
  platform_id integer REFERENCES public.platforms(id) ON DELETE CASCADE,
  department_id integer REFERENCES public.departments(id) ON DELETE CASCADE,
  min_role text CHECK (min_role IN ('member','leader')),
  user_id uuid REFERENCES public.users_new(id) ON DELETE CASCADE,
  is_system boolean DEFAULT false,
  created_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.department_route_access (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  department_id integer NOT NULL REFERENCES public.departments(id) ON DELETE CASCADE,
  route text NOT NULL,
  label text,
  created_at timestamptz DEFAULT now(),
  UNIQUE(department_id, route)
);

CREATE TABLE IF NOT EXISTS public.system_logs (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  created_at timestamptz DEFAULT now(),
  level text NOT NULL CHECK (level IN ('info','warn','error')),
  source text NOT NULL,
  action text NOT NULL,
  actor_id uuid REFERENCES public.users_new(id) ON DELETE SET NULL,
  target_id uuid,
  target_type text,
  metadata jsonb DEFAULT '{}'::jsonb
);
CREATE INDEX IF NOT EXISTS idx_system_logs_created_at ON public.system_logs(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_system_logs_actor ON public.system_logs(actor_id);

CREATE TABLE IF NOT EXISTS public.tools (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug text NOT NULL UNIQUE,
  name text NOT NULL,
  description text,
  icon text,
  category text,
  config jsonb DEFAULT '{}'::jsonb,
  sort_order integer DEFAULT 0,
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);


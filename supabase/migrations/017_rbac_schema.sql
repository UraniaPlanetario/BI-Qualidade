DO $$
DECLARE t text;
BEGIN
  FOR t IN SELECT unnest(ARRAY[
    'users_new','departments','platforms','user_departments','user_platform_access',
    'route_access_policies','route_access_rules','department_route_access','tools'
  ])
  LOOP
    EXECUTE format('DROP POLICY IF EXISTS read_all_authenticated ON public.%I', t);
    EXECUTE format('CREATE POLICY read_all_authenticated ON public.%I FOR SELECT USING (auth.role() = ''authenticated'' OR auth.role() = ''anon'')', t);
    EXECUTE format('GRANT SELECT ON public.%I TO anon, authenticated', t);
    EXECUTE format('GRANT ALL ON public.%I TO service_role', t);
  END LOOP;
END $$;

DROP POLICY IF EXISTS read_system_logs ON public.system_logs;
CREATE POLICY read_system_logs ON public.system_logs FOR SELECT USING (
  EXISTS (SELECT 1 FROM public.users_new u WHERE u.auth_user_id = auth.uid() AND u.is_global_admin = true)
);
GRANT SELECT ON public.system_logs TO authenticated;
GRANT ALL ON public.system_logs TO service_role;

-- Helper func
CREATE OR REPLACE FUNCTION public.is_bi_admin()
RETURNS boolean LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public AS $$
  SELECT EXISTS (SELECT 1 FROM public.users_new u WHERE u.auth_user_id = auth.uid() AND u.is_global_admin = true);
$$;
GRANT EXECUTE ON FUNCTION public.is_bi_admin() TO anon, authenticated, service_role;

-- Trigger handle_new_user adaptado (liga auth.users ao public.users_new por email)
CREATE OR REPLACE FUNCTION public.handle_new_auth_user()
RETURNS trigger LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE existing_id uuid;
BEGIN
  SELECT id INTO existing_id FROM public.users_new WHERE email = NEW.email;
  IF existing_id IS NOT NULL THEN
    UPDATE public.users_new SET auth_user_id = NEW.id, updated_at = now()
    WHERE id = existing_id AND auth_user_id IS DISTINCT FROM NEW.id;
  ELSE
    INSERT INTO public.users_new (id, auth_user_id, email, full_name, is_active)
    VALUES (NEW.id, NEW.id, NEW.email, COALESCE(NEW.raw_user_meta_data->>'full_name', NEW.email), true)
    ON CONFLICT (email) DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$;
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created AFTER INSERT ON auth.users
FOR EACH ROW EXECUTE FUNCTION public.handle_new_auth_user();


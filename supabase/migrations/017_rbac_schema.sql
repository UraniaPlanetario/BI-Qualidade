-- Policies de rota (sem policy = liberado)
-- Sayrana (role=user) vê só /comercial/qualidade e /comercial/monitoramento.
-- As demais rotas exigem platform_admin (manager/admin) ou global_admin.
DO $$
DECLARE bi_id integer; pol_id integer;
BEGIN
  SELECT id INTO bi_id FROM public.platforms WHERE slug = 'bi';
  FOR pol_id IN
    WITH routes AS (
      SELECT unnest(ARRAY[
        '/comercial/leads-fechados','/comercial/campanhas',
        '/comercial/desempenho-vendedor','/comercial/desempenho-sdr',
        '/financeiro','/admin/usuarios'
      ]) AS route,
      unnest(ARRAY[
        'Leads Fechados','Campanhas Semanais',
        'Desempenho Vendedor','Desempenho SDR',
        'Faturamento','Admin Usuários'
      ]) AS label
    )
    INSERT INTO public.route_access_policies (platform_id, route, label, managed_by)
    SELECT bi_id, route, label, 'global_admin' FROM routes
    ON CONFLICT (platform_id, route) DO UPDATE SET label = EXCLUDED.label
    RETURNING id
  LOOP
    INSERT INTO public.route_access_rules (policy_id, rule_type, is_system) VALUES (pol_id, 'global_admin', true);
    INSERT INTO public.route_access_rules (policy_id, rule_type, platform_id, is_system) VALUES (pol_id, 'platform_admin', bi_id, false);
  END LOOP;
END $$;

-- /admin/usuarios: só global_admin (remover platform_admin que liberaria manager)
DO $$
DECLARE admin_pol_id integer;
BEGIN
  SELECT rap.id INTO admin_pol_id
  FROM public.route_access_policies rap JOIN public.platforms p ON p.id = rap.platform_id
  WHERE p.slug = 'bi' AND rap.route = '/admin/usuarios';
  IF admin_pol_id IS NOT NULL THEN
    DELETE FROM public.route_access_rules
    WHERE policy_id = admin_pol_id AND rule_type = 'platform_admin';
  END IF;
END $$;
